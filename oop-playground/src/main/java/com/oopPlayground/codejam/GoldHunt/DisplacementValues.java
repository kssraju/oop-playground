package com.oopPlayground.codejam.GoldHunt;

public class DisplacementValues {
    private int x;
    private int y;

    public DisplacementValues(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
